    void acceder(String usuario, String pass)// metodo para acceder de un nuevo usuario de bd
    {
        String cap="";//creacion de varibles que mira el tipo de usuario que es 
       String sql="SELECT * FROM tab_usua WHERE nick_usua='"+usuario+"' && pass_usua='"+pass+"'";//busca en la bd la informacion 
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);//
            while(rs.next())// valida que los datos esten en la bd
            {
                cap=rs.getString("tap_usua");// mira el tipo de usuario 
            }
            if(cap.equals("ADMINISTRADOR"))// si es este tipo lo direcciona 
            {
                  this.setVisible(false);
                    JOptionPane.showMessageDialog(null, "BIENVENIDO");
                     menu_admi ingreso = new menu_admi();
                    ingreso.setVisible(true);
                    ingreso.pack();
            }
             if(cap.equals("PACIENTE"))// si es de este tipo direcciona a la venta de paciente 
            {
                  this.setVisible(false);
                    JOptionPane.showMessageDialog(null, "BIENVENIDO");
                     menu_paci ingreso = new menu_paci();
                    ingreso.setVisible(true);
                    ingreso.pack();
            }
            if(cap.equals("DOCTOR"))// si es de este tipo lo direcciona a la ventana de doctor 
            {
            this.setVisible(false);
                    JOptionPane.showMessageDialog(null, "BIENVENIDO");
                     menu_doct ingresos = new menu_doct();
                    ingresos.setVisible(true);
                    ingresos.pack();
                    
            }
            if((!cap.equals("ADMINISTRADOR"))&& (!cap.equals("DOCTOR"))&& (!cap.equals("PACIENTE"))) // valida que aparezcan si no aparecen 
            {
                JOptionPane.showMessageDialog(this, "LOS DATOS ESTAN INCORRECTOS");// este es el mensaje que le aparece 
            }
        } catch (SQLException ex) {
            Logger.getLogger(inic_usua.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        }
